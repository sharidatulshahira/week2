import java.util.*;
public class Exercise39 {
	
	public static void printStars(int amount){
		for (int i=1; i<=amount; i++){
			System.out.println("*");
		}
		
	}

	
	public static void main(String[] args) {
		printStars(5);
		printStars(3);
		printStars(9);
		
	}


}
